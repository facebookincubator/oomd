const React = require('react');

const CompLibrary = require('../../core/CompLibrary.js');
const MarkdownBlock = CompLibrary.MarkdownBlock; /* Used to read markdown */
const Container = CompLibrary.Container;
const GridBlock = CompLibrary.GridBlock;

const siteConfig = require(process.cwd() + '/siteConfig.js');

function imgUrl(img) {
  return siteConfig.baseUrl + 'img/' + img;
}

function docUrl(doc, language) {
  return siteConfig.baseUrl + 'docs/' + (language ? language + '/' : '') + doc;
}

function pageUrl(page, language) {
  return siteConfig.baseUrl + (language ? language + '/' : '') + page;
}

class Button extends React.Component {
  render() {
    return (
      <div className="pluginWrapper buttonWrapper">
        <a className="button" href={this.props.href} target={this.props.target}>
          {this.props.children}
        </a>
      </div>
    );
  }
}

Button.defaultProps = {
  target: '_self',
};

const SplashContainer = props => (
  <div className="homeContainer">
    <div className="homeSplashFade">
      <div className="wrapper homeWrapper">{props.children}</div>
    </div>
  </div>
);

const Logo = props => (
  <div className="projectLogo">
    <img src={props.img_src} />
  </div>
);

const ProjectTitle = props => (
  <h2 className="projectTitle">
    {siteConfig.title}
    <small>{siteConfig.tagline}</small>
  </h2>
);

const PromoSection = props => (
  <div className="section promoSection">
    <div className="promoRow">
      <div className="pluginRowBlock">{props.children}</div>
    </div>
  </div>
);

const ProjectGitHubStats = props => (
  <div>
    <a
      className="github-button"
      href={siteConfig.repoUrl}
      data-icon="octicon-star"
      data-count-href="/facebookmicrosites/cgroup2/stargazers"
      data-show-count={true}
      data-count-aria-label="# stargazers on GitHub"
      aria-label="Star this project on GitHub">
      Star
    </a>
  </div>
);

const ProjectDescription = props => (
  <div>
    <p>Use memory pressure thresholds for greater control during OOM events. </p>
  </div>
);

class HomeSplash extends React.Component {
  render() {
    let language = this.props.language || '';
    return (
      <SplashContainer>
        <div className="inner">
          <ProjectTitle />
          <ProjectDescription />
          <PromoSection>
            <Button href={docUrl('overview', language)}>Get Started</Button>
          </PromoSection>
          <ProjectGitHubStats />
        </div>
      </SplashContainer>
    );
  }
}

const Block = props => (
  <Container
    padding={['bottom', 'top']}
    id={props.id}
    background={props.background}>
    <GridBlock align="center" contents={props.children} layout={props.layout} />
  </Container>
);

const Features = props => (
  <Block layout="fourColumn">
    {[
      {
        content: 'Kill memory-hogging processes when PSI memory pressure exceeds a specified threshold.',
        imageAlign: 'top',
        title: 'Memory Pressure Triggered OOM Killer',
      },
      {
        content: 'Works on a per-cgroup basis, allowing the killing of an entire groups of processes without compromising the system or the main workload.',
        imageAlign: 'top',
        title: 'Built on cgroup2',
      },
      {
        content: 'Deal more proactively and gracefully with increasing memory pressure by pausing some tasks to ride out the bump, an app shutdown with a scheduled restart, or any other specified actions.',
        imageAlign: 'top',
        title: 'More Granular OOM-Kill Options',
      },
      
    ]}
  </Block>
);


const Showcase = props => {
  if ((siteConfig.users || []).length === 0) {
    return null;
  }
  const showcase = siteConfig.users
    .filter(user => {
      return user.pinned;
    })
    .map((user, i) => {
      return (
        <a href={user.infoLink} key={i}>
          <img src={user.image} title={user.caption} />
        </a>
      );
    });

  return (
    <div className="productShowcaseSection paddingBottom">
      <h2>{"Who's Using This?"}</h2>
      <p>This project is used by all these people</p>
      <div className="logos">{showcase}</div>
      <div className="more-users">
        <a className="button" href={pageUrl('users.html', props.language)}>
          More {siteConfig.title} Users
        </a>
      </div>
    </div>
  );
};

class Index extends React.Component {
  render() {
    let language = this.props.language || '';

    return (
      <div>
        <HomeSplash language={language} />
        <div className="mainContainer">
          <Features />
          <Showcase language={language} />
        </div>
      </div>
    );
  }
}

module.exports = Index;
