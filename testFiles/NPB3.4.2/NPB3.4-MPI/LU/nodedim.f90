
!---------------------------------------------------------------------
!---------------------------------------------------------------------

      integer function nodedim(num)

!---------------------------------------------------------------------
!---------------------------------------------------------------------

!---------------------------------------------------------------------
!
!  compute the exponent where num = 2**nodedim
!  NOTE: assumes a power-of-two number of nodes
!
!---------------------------------------------------------------------

      implicit none

!---------------------------------------------------------------------
!  input parameters
!---------------------------------------------------------------------
      integer num

!---------------------------------------------------------------------
!  local variables
!---------------------------------------------------------------------
      double precision fnum


      fnum = dble(num)
      nodedim = log(fnum)/log(2.0d+0) + 0.00001

      return
      end


